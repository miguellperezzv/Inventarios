public class Proveedor {
    
    int k_proveedor;
    String n_proveedor;
    String c_correo;
    int c_telefono;

    public Proveedor() {
    }

    public int getK_proveedor() {
        return k_proveedor;
    }

    public void setK_proveedor(int k_proveedor) {
        this.k_proveedor = k_proveedor;
    }

    public String getN_proveedor() {
        return n_proveedor;
    }

    public void setN_proveedor(String n_proveedor) {
        this.n_proveedor = n_proveedor;
    }

    public String getC_correo() {
        return c_correo;
    }

    public void setC_correo(String c_correo) {
        this.c_correo = c_correo;
    }

    public int getC_telefono() {
        return c_telefono;
    }

    public void setC_telefono(int c_telefono) {
        this.c_telefono = c_telefono;
    }
    
    
}
